<h4>資料をアップロードする</h4>
<ul>
<li>左上の【ファイルのアップロード】をクリックします。</li>
<li>【参照】を押してコンピューター内のファイルを選択します。</li>
<li>選択したファイルの説明文を記載することもできます。
<blockquote><small>
