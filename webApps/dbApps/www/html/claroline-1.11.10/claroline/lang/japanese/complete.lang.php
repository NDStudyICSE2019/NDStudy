<ul>
<li>【外部リンク作成】をクリックします。</li>
<li>【名前】にリンク名を記入します（※現在のところ日本語は使えません！下を参照）。</li>
<li>リンクのURLを記入します（例：http://my.domain/my/file.htm）。</li>
<li>【OK】をクリックして確定します。</li>
<li>※このリンクに【解説】をつけることもでき（※日本語も表示可能）、ファイル名の下の行に表示されます。</li>
