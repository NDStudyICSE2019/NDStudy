<ul>
<li>上部にある【文書作成(HTMLファイル)】をクリックします。</li>
<li>【ファイル名：】に作成する資料の名前を記入します（※現在のところ日本語ファイル名は使えません！下を参照）。</li>
<li>【内容】に資料の本文を記入します。</li>
<li>作成が終わったら【OK】をクリックします。</li>
<li>※ファイル・リストに新たに追加されたファイルの【編集】をクリックし、【解説】を記入しておくと英語ファイル名の下にその概要が表示されます。</li>

</ul>
<p>
このコマンドは短い資料を作成するのに向いていますが、もっと長い資料を作成するには使い慣れたワープロソフトなどを利用した方が良いでしょう。
</p>
<h4>資料、フォルダ、リンク名を変更する</h4>
<ul>
<li>ファイルの【編集】をクリックします。</li>
<li>左上の【名前】に新しいファイル名を入力します。</li>
<li>このファイルに解説をつけることもでき（※日本語も表示可能）、ファイル名の下の行に表示されます。</li>
<li>【OK】をクリックして新しいファイル名を確定してください。</li>
</ul>
<p>
<b>Tips </b>：資料はアルファベット順に表示されますので、01.xxxx、02.xxxx、10.xxxx、11.xxxxのように番号を振っておくと良いでしょう。</p>
<h4>資料、フォルダ、リンクを削除する</h4>
<ul>
<li>資料を削除するには、横にあるアイコンをクリックします。</li>
</ul>
<h4>
資料、フォルダ、リンクを非公開に公開する
</h4>
<ul>
<li>資料横の【表示モード】アイコンをクリックします。</li>
<li>資料そのものは削除されませんが、学生向けには表示されなくなります。</li>
<li>公開するには、もう一度【表示モード】アイコンをクリックします。</li>
</ul>
<h4>
資料、フォルダ、リンクに解説を追加する
</h4>
<ul>
<li>資料横の【編集】をクリックします。</li>
<li>【解説】に資料の概要を記入します。</li>
<li>【OK】をクリックして概要を確定してください。</li>
</ul>
<p>
解説文を削除するには【編集】をクリックして、【解説】の内容を削除し【OK】をクリックしてください。</p>
<h4>フォルダを作成する</h4>
<ul>
<li>【フォルダ作成】をクリックします。</li>
<li>フォルダ名を入力します（※現在のところ日本語ファイル名は使えません！下を参照）。</li>
<li>【OK】をクリックし確定してください。</li>
<li>※【解説】に日本語の概要を記入しておくことをお勧めします。</li>
</ul>
<h4>
資料、フォルダ、リンクを移動する
</h4>
<ul>
<li>資料横の【移動】アイコンをクリックします。</li>
<li>移動させたいフォルダをスクロールメニュー（左上にある）から選択します。
<br />注意：「root」は資料フォルダのトップレベルで、これより上の階層には移動できません。</li>
<li>【OK】をクリックし確定してください。</li>
</ul>
